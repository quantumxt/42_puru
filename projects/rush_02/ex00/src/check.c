#include "check.h"

int	is_space(char c)
{
	return (c == ' ' || (c >= '\t' && c <= '\r'));
}

int	is_sign(char c)
{
	return (c == '+' || c == '-');
}

int	is_num(char c)
{
	return (c >= '0' && c <= '9');
}

int	is_non_printable(char c)
{
	return (c < ' ' || c > '~');
}
