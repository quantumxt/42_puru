#include <unistd.h>

void	print_str(char *str)
{
	while (*str)
		write(1, str++, 1);
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	pos;

	pos = 1;
	while (pos < argc)
		print_str(argv[pos++]);
	return (0);
}
