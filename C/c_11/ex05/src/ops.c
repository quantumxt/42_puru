#include "ops.h"

int	add(int a, int b)
{
	return (a + b);
}

int	subtract(int a, int b)
{
	return (a - b);
}

int	multiply(int a, int b)
{
	return (a * b);
}

int	division(int a, int b)
{
	return (a / b);
}

int	modulo(int a, int b)
{
	return (a % b);
}
