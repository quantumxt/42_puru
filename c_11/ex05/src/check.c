#include "check.h"

int	check_space(char c)
{
	return ((c >= 9 && c <= 13) || c == ' ');
}

int	check_operator(char c)
{
	return ((c == '+') | (c == '-') | (c == '*') | (c == '/') | (c == '%'));
}
